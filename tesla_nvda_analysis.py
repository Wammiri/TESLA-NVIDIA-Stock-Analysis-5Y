"""### NumPy Calculations"""

stock_close = stock_data.values

# Daily returns
daily_rtn = (stock_close[1:] - stock_close[:-1]) / stock_close[:-1]

# Mean return & volatility
mean_rtn = daily_rtn.mean(axis=0)
