plt.plot(nvda_growth, label="100% NVIDIA")
plt.plot(portfolio_5050, label="50/50 Portfolio (monthly rebalance)")
plt.title("Portfolio Growth: $10,000 (5 Years)")
plt.ylabel("Portfolio Value ($)")
